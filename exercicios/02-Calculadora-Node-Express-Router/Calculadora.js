
function soma(a,b){
    return a + b
}

function subtrair(a,b){
    return a - b
}

function multiplicar(a,b){
    return a * b
}

function divisao(a,b){
    return a / b
}

function aoQuadrado(a,b){
    return a ** 2
}

function raizQuadrada(a){
    return Math.sqrt(a)
}

module.exports = {
    soma,
    subtrair,
    multiplicar,
    divisao,
    aoQuadrado,
    raizQuadrada
}
